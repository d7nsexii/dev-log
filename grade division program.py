# Grade division system
# Try and see your grade



username = input(" Enter your name : " )
grade = int(input(" Enter your score : " ))
if grade >= 95:
    print(f"{username}, Your score is {grade}. and you get S.")
elif grade >= 90:
    print(f"{username}, Your score is {grade}. and you get A.")
elif grade >= 80:
    print(f"{username}, Your score is {grade}. and you get B.")
elif grade >= 70:
    print(f"{username}, Your score is {grade}. and you get C.")
else:
    print(f"{username}, Your score is {grade}. and you get D or E.")


